#import "PaymentEntity.h"

@implementation PaymentEntity

- (instancetype)initWithAttributes:(NSDictionary *)attributes
{
    self = [super init];
    
    if (!self) {
        return nil;
    }
    
    self.receive_id = [attributes valueForKey:@"receive_id"];
    
    self.title = [attributes valueForKey:@"title"];
    
    self.time = [attributes valueForKey:@"time"];
    
    self.create_time = [attributes valueForKey:@"create_time"];
    
    self.user_id = [attributes valueForKey:@"user_id"];
    
    self.company_name = [attributes valueForKey:@"company_name"];
    
    self.num = [attributes valueForKey:@"num"];
    
    self.remarks = [attributes valueForKey:@"remarks"];
    
    self.user_name = [attributes valueForKey:@"user_name"];
    
    return self;
    
}

@end
