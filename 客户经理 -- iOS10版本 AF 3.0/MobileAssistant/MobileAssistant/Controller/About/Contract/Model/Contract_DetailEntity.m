#import "Contract_DetailEntity.h"

@implementation Contract_DetailEntity

- (instancetype)initWithAttributes:(NSDictionary *)attributes{
    
    self = [super init];
    
    if (!self) {
        return nil;
    }

    self.contract_id = [attributes valueForKey:@"contract_id"];
    self.company_name = [attributes valueForKey:@"company_name"];
    self.company_num = [attributes valueForKey:@"company_num"];
    self.user_num = [attributes valueForKey:@"user_num"];
    self.contract_name = [attributes valueForKey:@"contract_name"];
    self.contract_sn = [attributes valueForKey:@"contract_sn"];
    self.start_time = [attributes valueForKey:@"start_time"];
    self.end_time = [attributes valueForKey:@"end_time"];
    
    return self;
}

@end
