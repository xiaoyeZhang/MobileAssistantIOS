#import "visit_task_summaryModel.h"

@implementation visit_task_summaryModel

- (instancetype)initWithAttributes:(NSDictionary *)attributes{
    
    self = [super init];
    if (!self) {
        return nil;
    }
    
    self.times = [attributes valueForKey:@"times"];
    self.actor_id = [attributes valueForKey:@"actor_id"];
    self.actor_num = [attributes valueForKey:@"actor_num"];
    self.company_name = [attributes valueForKey:@"company_name"];
    
    return self;
}

@end
