#import "Product_informationEntity.h"

@implementation Product_informationEntity

- (instancetype)initWithAttributes:(NSDictionary *)attributes{
    
    self = [super init];
    if (!self) {
        return nil;
    }
    
    self.type_id = [attributes valueForKey:@"type_id"];
    self.name = [attributes valueForKey:@"name"];
    self.scope_id = [attributes valueForKey:@"scope_id"];
    self.icon = [attributes valueForKey:@"icon"];
    self.color = [attributes valueForKey:@"color"];

    return self;
}

@end
