    if (!self) {
        return nil;
    }
    
    self.num = [attributes valueForKey:@"num"];
    self.name = [attributes valueForKey:@"name"];
    self.user_id = [attributes valueForKey:@"user_id"];
    self.tel = [attributes valueForKey:@"tel"];
    
    return self;
}

