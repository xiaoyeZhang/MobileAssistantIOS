#import "Marking_listEntity.h"

@implementation Marking_listEntity

- (instancetype)initWithAttributes:(NSDictionary *)attributes{

    self = [super init];
    if (!self) {
        return nil;
    }
    
    self.marketing_id = [attributes valueForKey:@"marketing_id"];
    self.name = [attributes valueForKey:@"name"];

    self.level = [attributes valueForKey:@"level"];    
    
    self.end_time = [attributes valueForKey:@"end_time"];
    self.user_name = [attributes valueForKey:@"user_name"];
    
    self.num = [attributes valueForKey:@"num"];
    self.start_time = [attributes valueForKey:@"start_time"];
    
    self.update_time = [attributes valueForKey:@"update_time"];
    
    return self;
}
@end
